    value.each_with_index do |card, index|
      new_card = Card.create(card_type:"minor", value:index+1, suit:key, name:card[:name], meaning_up:card[:meaning_up], meaning_rev:card[:meaning_rev], desc:card[:desc])
    end
  end
end

